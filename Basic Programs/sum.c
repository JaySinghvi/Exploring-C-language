// Program to accept 2 numbers from user and sum them

#include <stdio.h>

 int main()
 {
     int n1, n2, sum;

     printf("Enter 2 numbers to sum:\t");
     scanf("%d%d", &n1, &n2);

     sum = n1 + n2;

     printf("The Sum of 2 numbers is %d", sum);

     return 0;




 }

